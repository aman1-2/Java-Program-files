public class ifElse_17 {
    /*If-Else is a Conditional Statement which does it works if the condition evaluates to true
 * does the following task and if its comes out to be false execute its else part.
 * 
 * Statements which are not true for if part will definately comes out to be true for else part.
 */

    public static void main(String[] args) {
        int age=17;
        if(age>=18){ //If the condition Evaluates to true the If block statements will gat executed.
            System.out.println("You can Go to Bars!!!");
        }   else{// otherwise the else statements would get executed..
            System.out.println("You are still not Adult you cann't be allowed to go Bars!!!");
        }
    }

 }